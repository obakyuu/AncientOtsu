        日本の古代史のように、現在も調査を続けています。それらの遺跡は例外ではないでしょうか。
    </p>
    <h3 class="uk-heading-bullet">私が古代史に惹かれた理由</h3>
    <h4>
        ※ここからは自論です。ドキュメント本来の目的には沿わないですが、
        折角の機会だったのでこの場で雑談形式で記述することにしました。
    </h4>
    <p>
        私は、生まれも育ちもここ大津市です。
        滋賀は大阪、兵庫、京都と並んで発展しているとは思いませんが、滋賀には滋賀の良さがある。
        少しの誇りは私にはあります。私が小学４年生の頃、夏にそろそろ入る7月頃に社会科の授業の一環で
        『地元の歴史を知ろう！』という歴史の校外学習みたいなものがありました。
    </p>
    <p>
        当時の私は、地元の歴史については全然興味はなかったのですが、
        海外の古代文明にはちょっと興味を持っていました。エジプトのピラミッドがどのようにして造られたのか。
        象形文字の意味。隠された秘宝、ツタンカーメンなど、インディージョーンズは見たことはありませんが、
        大昔の遺物にロマンを当時の私はありました。
    </p>
    <p>
        校外学習では、学校すぐ近くにある古墳に行きました。
        その古墳を初めて見たときは圧巻でした。社会の教科書にある古墳は、有名な前方後円墳、円墳、方墳、
        変わったもので双方墳が掲載されてましたが、ドーム状の古墳はその古墳を見るまでは全く知りませんでした。
        大きな巨石。複雑に組まれてた石室、上に行くほど窄み、不思議なバランスで 1,400 年ほど、
        その形を保ち、周りは竹藪に覆われていたということもあり、周囲からはそこに古墳があると
        認知するのは難しいと思います。なぜこんな不思議な構造物があることを近所にする私が知らなかったのか。
        不思議には思いましたが、さらに驚いたのがそれが１つだけではなく、周辺にもっと点在していたという
        点でした。「まさか自分が住んでいる町ってすごい歴史があったりするんじゃない？ 」と興味を持ち、
        当時パソコンの操作をやっと覚えた私が、インターネットを使って調べまくりました。
    </p>
    <p>
        そこから各所の詳しい史跡情報を知り、自転車で現地に赴き、夏休みの自由研究は、学校周辺だけでなく、
        もうちょっと範囲を広げ、大津の古墳時代の史跡群の場所と情報をまとめた研究を行いました。
        研究してわかったのが、その史跡すべてがほぼ同じ時代に造られたものであり、共通性がある点が面白く、
        自分が暮らしている足元には、昔確実に人がいたと想像力に掻き立てられました。
    </p>
    <p>
        しかし、大津はすべての遺跡を発見したわけではありません。大津は奈良の平城京、京都の平安京のように、
        大津宮が存在し、滋賀は首都でした。そして首都だった以前に朝鮮系の渡来人が大津に訪れており、
        当時としては国際色豊かな地域であったと考えています。今残されているドーム状古墳がその代表例で、
        そもそもドーム状に造る古墳は 日本古来の古墳ではなく、朝鮮民族の渡来人に影響されたものであり、
        同時に中から出土したミニチュア竈門セットも渡来人の影響です。こうして各地に異文化が定着、広まり、
        その名残が今の遺跡に残されている。というわけです。人口が多かったこともあり遺跡もその分増え、
        <b>今も住宅地の下に埋没しているかもしれない。まだ見つけられていない物を探すロマン、
        埋没品からここには何があったのか？それを考察することが、この古代史の最大の魅力なのかもしれません。</b>
        若干熱くなりましたが、以上が私が古代史に惹かれるまでの過程でした。
    </p>
    <h3 class="uk-heading-bullet">私が思う大津の魅力</h3>
    <p>
        滋賀と言ったら一番は『琵琶湖』ですね。よく滋賀人は何かあると「琵琶湖の水止めるぞ！」と脅しをかけがちな
