    </p>
    <h3 class="uk-heading-bullet">弁明</h3>
    <p>
        先刻、古代の史料は多くはないと発言しましたが、全くないというわけでもありません。
        代表的なものは土器です。ここ日本（北海道と沖縄を除く本州と九州、特に中国寄りほど）の地下には
        まだ土器が埋没しています。土器は紙とは違い劣化による消失はあまりありません。
        色の再現は難しいかもしれませんが、形はほぼ当時のものを再現することが可能です。
        他には、建造物の跡があります。建物跡を見つけるためには、土層、土の色を見分ける能力が必要です。
        僅かな違いを区別することで、当時の建物跡を特定できます。そして、古代といえば有名なものはもう1つ。
        古墳です。古墳とは今でいう墓のことです。古墳はアフリカのピラミッドのように、
        有力な権力者及び一族を埋葬するために造られた権威の象徴です。一般人は原則古墳には埋葬されません。
        古墳からは、具体的に誰が埋葬されたのか等の人物を完全に特定することは非常に難しいですが、
        その古墳の形式で作られた時代の推定、規模でどれほど権力があったか、
        葬品で当時の文化がわかるようになります。あとは、上記３つ以外に石器や貝塚とかも見つかります。
        それらで当時の人たちがどんな生活を営んでいたのかわかるようになります。
    </p>
    <h3 class="uk-heading-bullet">古代史のあるべき姿</h3>
    <p>
        古代史は今よりもっと明確な事実を把握するよう努めるべきです。理由は、日本がどのように誕生したか？
        その核心に迫る重要な時代でもあるからです。歴史とは明確にわかっている事実を周知、
        後世に伝承する学問であるとするなら、この古代史は不明確なことが多いです。
        この古代史を歴史として取り入れるに値するように、調査、解析を続行するべきであると私は考えます。
    </p>
    <h3 class="uk-heading-bullet">冒頭の例外について</h3>
    <p>
        日本国外における世界史については、私が無知であることを理由に詳しくは言及しませんが、
        古代史に需要がある場合は例外だと思います。
        実際、日本の古代史は最近までは需要はないように見えました。2019年7月6日にアゼルバイジャンで
        開かれた世界遺産委員会で大阪府堺市にある<b uk-tooltip="もずこふんぐん">百舌鳥古墳群</b>
        (大仙古墳)が世界遺産に登録され、以前より多くの観光客が百舌鳥古墳群に足を運ぶようになりました。
        それに伴い観光業としての栄え、新たな観光資源として脚光を浴びることとなりました。日本国外を
        例にすると、海を隔てた隣国、中国の場合では秦時代から造り始められた万里の長城、欧州の場合は
        古代ローマのコロッセオを含む当時の街並みアフリカはギザのピラミッドで有名なエジプト文明、
        古代とは言えませんが南米のインカ帝国の遺跡、マチュ・ピチュ。どちらも観光業として有名な遺跡です。
        特にエジプトのピラミッドを含めたエジプト文明は、以前まだわからないこともあり
