        琵琶湖花火、冬はびわ湖バレイや箱館山のスキー場、歴史では有名な武将『織田信長』の安土城。
        NHK 大河ドラマ『麒麟が来る』の明智光秀にゆかりのある『西教寺』。
        よく京都の世界遺産として勘違いされがちの比叡山の『延暦寺』 があります。
        今でこそ自動車や電車の交通網が発展してしまったために、滋賀は
        <b uk-tooltip="実際、田舎であることは揺るぎないのも事実ですが...">田舎扱い</b>
        されますが、それ等がなかった昔は、交通の要衝としての側面を持ち合わせていました。
        結局魅力に思うところは、人の感性によってまちまちですが、上記の何かに魅力を持つことができたのであれば、
        それが魅力になるのではないでしょうか？ 町に今も眠る『古代遺跡』も覚えてもらえれば幸いです。
    </p>
    <h3 class="uk-heading-bullet">大津市民の遺跡に対する関心について</h3>
    <p>
        残念な話ですが、大津市民の
        <b uk-tooltip="割合は私の妄想です。ただ、過半数は関心がないのは事実です">9割</b>
        は地元の歴史には何も関心はないと思います。それを裏付けるように、
        普通は史跡の近くの見える場所に看板を立ててあったりしますが、大津市の史跡は看板がある史跡の方が
        少ないです。なぜ看板がないのかは以下の理由があります。
        <ol>
        <li>遺跡が多すぎて看板を立てるコストが高くなる</li>
        <li>歴史的価値があまりない</li>
        <li>昔は確かにあったが宅地造成や道路造成で取り壊された</li>
        </ol>
        上記の3つの理由が原因で、近くに住む住民がそこに遺跡があったことを知る機会がなかった。
        と考えても不思議ではありません。仮に機会はあっても興味を持つかもわからないです。
        宅地や道路造成で取り壊しになるのは惜しいと思う方もいるかもしれませんし、いないかもしれません。
        実際、仮にその遺跡を残したとして、お金になるわけでもありませんし、家や道路を作れば、
        将来的には経済を回せる。と考えてしまうのは仕方がないのかなぁ。と思います。
        逆説すると、遺跡を作った当時の人たちは果たして<b>『今の我々に向けて遺跡を造ったのか？』</b>と
        問われると、たぶん違うんじゃないかと思います。当時の人たちはあくまで、
        自分たちが便利に生活するため、菩提を弔うため、権威を象徴するため。
        <b>その当時の人たちの努力の賜物が偶然、今も残っているだけ。</b>
        と考えてもおかしくはないかもしれません。<br>
    </p>
