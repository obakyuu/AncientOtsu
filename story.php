<div class="uk-container uk-section uk-animation-fade uk-margin-medium-bottom<?= $style[0] ?>">
    <h1 class="uk-text-center">古代史を学ぶことの意義</h1>
    <hr class="uk-divider-icon uk-height-max-small">
    <h3 class="uk-heading-bullet">結論</h3>
    <p>
        先に結論を述べますと、古代史を学ぶ意味はさほどありません。
    </p>
    <h3 class="uk-heading-bullet">謝罪</h3>
    <p>
        失言してしまい大変申し訳ありませんでした。考古学者、歴史学者、歴史センター、博物館に勤務する従業員、
        教育委員会、現場の発掘調査員、発掘に協力した関係者、皆様の努力を踏みにじる発言だったことと、
        後世に地元の歴史伝承に努める当該地域の住民皆様に深い傷をつけたことをお詫び申し上げます。
    </p>
    <h3 class="uk-heading-bullet">古代史ってなに？</h3>
    <p>
        古代史とは歴史の中の一区分のことを指します。古代の他に、中世、近世の3つがあります。
        歴史とは学校で習う授業の１つです。小中高で歴史の授業（日本が舞台の場合は日本史、海外の場合は世界史）
        は学ぶはずです。古代史とは、その歴史の一区分のことであり、そしてその区分の中で最も古い時代に位置します。
    </p>
    <h3 class="uk-heading-bullet">日本の古代史</h3>
    <p>
        古代には、中世、近世とは異なる特徴を持っています。すべてに当てはまるわけではありませんが、
        大半は当時の文字史料がほぼないということです。日本の場合は、中世、近世は大抵、紙の史料が
        今も残っています。一部は大火によって焼失してしまった事例もありますが、今も大切に保管されていることもあります。
        一方、古代となるとそういった文字史料がほぼ残されていません。その為、中世、近世と比べて
        まだ古代にはわからないことが多いのです。有名な話として一説を上げると『邪馬台国はどこにあったのか？』
        『朝廷がいつ誕生したのか？』が挙げられます。ここで述べるのは妥当ではありませんが、
        古代史がいつからを始まりとするかも、実は決まっていません。
        『七五三論争』という日本国の誕生についてを定義する話があります。
        3世紀からか、5世紀からか、7世紀からか？ この議論は以前からされていたそうですが、
        未だ結論には至っていません。理由は判断材料が少ないからです。
        <b>もしかするとあなたが学んだ古代史は真実と異なっているのかもしれません。</b>
    </p>
    <h3 class="uk-heading-bullet">なぜ古代史を学ぶのか？</h3>
    <h4>―理由を述べる前に、少し寄り道をさせてください。</h4>
    <p>
        当サイトを訪れた人は少なからず、歴史が大嫌いであるということはたぶんないと思いますが、
        この世の中に歴史が嫌いだという人は確かにいます。個人的には暗記物が苦手な人は、
        歴史も嫌いという印象が私にはあります。私はどちらかというと歴史は好きですが、
        中世以降は嫌いとまではいきませんが、あんまり好きでもありません。
    </p>
    <h4>―なぜ歴史を学ぶ必要があるか？</h4>
    <p>
        この問いに対しては答えられる人はいると思います。
        実際ネットで検索しても出てきます。歴史を学ぶメリットは『過去の傾向と推論の精度を上げられるから』です。
        今と昔には確実に言える共通点があります。それは『どちらも舞台が地球で起きた出来事』であることです。
        その歴史の主役や環境は違うかもしれませんが、これだけは確かに言えることです。
        そして、もしそれ以外の部分で共通点が多かった場合、「今この場と状況が似ている。」と
        気づくことができます。これがわかると、これから何が起きるのか？どう対策をすればいいのか？と
        考える機会と、考察力の精度向上が期待できます。なので、歴史を学ぶことに意味はあります。
    </p>
    <h4>―なぜ古代を学ぶ必要があるか？</h4>
    <p>
        ここから本題です。この問いに答えられる人はいますか？ 結論を言うと私は未だ答えが出ません。
        大抵の人は「それは歴史を学ぶことと意味は同じではないか？」と述べる可能性がありますが、
        大きな違いがあります。それは<b>『今もよくわかってないことがある』</b>ことです。
        中世、近世では紙で『何があったのか？誰が関与しているのか？』等が記述されていると思います。
        これらの紙媒体は一例ではありますが、神社や寺に奉納されていたとか誰かが大切に保管していた。
        が挙げられます。ただ時代が古なればなるほど保存率は下がります。
        ところで、 私はまだ日本の 古代史がいつが終わりなのかについては述べてはいません。
        日本の古代の終わりは平安時代の頃（西暦1000 年頃）が主流です。古代史における史料は
        最低1000年以上もその史料を保持し続けなければならないのです。1000年以上も前の史料は
        正直多くはありません。なので、今の我々が考察しようにも、そもそも判断材料が少ないのです。
        なので歴史精度が弱くなり、<b>あくまで憶測で止まってしまうのです。</b>
        序論で私が学ぶ意味がないと発言した理由は、歴史学的観点から見て<b>古代史は
        それが正しいという確証があまりなく、今学んでいる古代史は一部間違いがある可能性が
        否定できないからです</b>
    </p>
    <h3 class="uk-heading-bullet">古代史は無視するべき？</h3>
    <h4>「なら、よくわかっていない時代なんて無視すればいいのではないか？」</h4>
    <p>
        と思うかもしれませんが、<b>『よくわからないというだけの理由』</b>では除外はできません。
        日本には古代は確かに存在し、その時代には<b class="uk-text-danger">『重大なことが起きた』</b>という事実だけはわかっているので、
        それを野放しにはできません。世の中は、気になることがあったら知りたいからです。
        そこに興味を持つか持たないかは、あなたの感性によりますが、少なからず
        <b class="uk-text-danger">常識の範囲として知っておくべき知識だと世の中は認知しているので学校で学ぶのです。</b>
